import turtle
slowpoke = turtle.Turtle()
slowpoke.fillcolor('green')
slowpoke.shape('turtle')

slowpoke.forward(300)
slowpoke.left(90)
slowpoke.forward(300)
slowpoke.left(90)
slowpoke.forward(600)
slowpoke.left(90)
slowpoke.forward(600)
slowpoke.left(90)
slowpoke.forward(600)
slowpoke.left(90)
slowpoke.forward(300)
slowpoke.left(90)
slowpoke.forward(300)
slowpoke.right(7*360)

turtle.mainloop()